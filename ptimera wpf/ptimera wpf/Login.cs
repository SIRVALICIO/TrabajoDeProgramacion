namespace ptimera_wpf
{
    public class Login
    {

        private string Usuario;
        private string Contraseña;

        public string usuario
        {
            get
            {
                return Usuario;
            }
            set
            {
                Usuario = value;

            }
        }

        public string contraseña
        {
            get
            {
                return Contraseña;
           
            }
            set
            {
                Contraseña = value;

            }
        }
       
        
    }
}